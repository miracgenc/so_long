#include "ft_printf.h"

void	ft_putnbr(int n, long *i)
{
	if (n < 0)
	{
		ft_putchar('-', i);
		if (n == -2147483648)
		{
			ft_putchar('2', i);
			n = -147483648;
		}
		n *= -1;
	}
	if (n > 9)
		ft_putnbr(n / 10, i);
	ft_putchar((n % 10) + '0', i);
}
